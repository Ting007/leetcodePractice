class Solution:
    def longestValidParentheses(self, s):
        """
        :type s: str
        :rtype: int
        """
        longest = [0]
        i = 0
        j = len(s)
        while len(s) - i > max(longest):
        	if s[i] == ')':
        		i += 1
        		j = len(s)
        	elif s[j-1] == '(' and j-i > max(1, max(longest)):
        		j -= 1
        	else:
        		sub_s = s[i:j]
        		# print(sub_s)
        		if self.validParentheses(sub_s):
        			longest.append(len(sub_s))
        			i += 1
        		elif j-i > max(max(longest), 1):
        			j -= 1
        		else:
        			i += 1
        			j = len(s)
        return max(longest)


    def validParentheses(self, s):
    	stack = []
    	for i in s:
    		if i == '(':
    			stack.append(i)
    		else:
    			if stack == []:
    				return False
    			else:
    				stack.pop()
    	return stack==[]

